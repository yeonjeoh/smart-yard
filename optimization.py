import pulp

# ── 1. 문제 정의 ──────────────────────────────────────────
prob = pulp.LpProblem("수리창_5척_인력배분_최적화", pulp.LpMinimize)

# ── 2. 결정변수 선언 ──────────────────────────────────────
# 각 함정 배치 인력 (정수, 최소 인원 적용)
x1 = pulp.LpVariable("PKG719_천안함", lowBound=3, cat='Integer')  # 기관부
x2 = pulp.LpVariable("FFG815_충남함", lowBound=2, cat='Integer')  # 기관부
x3 = pulp.LpVariable("SS083_나대용함", lowBound=2, cat='Integer') # 전자부
x4 = pulp.LpVariable("PKG711_독도함", lowBound=2, cat='Integer')  # 전자부
x5 = pulp.LpVariable("FF951_울산함",  lowBound=2, cat='Integer')  # 무장부

# ── 3. 목적함수 ───────────────────────────────────────────
# 가중 진도율 최대화 = 음수로 최소화
# 가중 진도율 = 우선순위 × 진도율 × 배치인력
prob += -(
    5 * 5 * x1 +   # PKG-719: 가중치5 × 진도율5%
    4 * 4 * x2 +   # FFG-815: 가중치4 × 진도율4%
    3 * 6 * x3 +   # SS-083:  가중치3 × 진도율6%
    2 * 4 * x4 +   # PKG-711: 가중치2 × 진도율4%
    1 * 3 * x5     # FF-951:  가중치1 × 진도율3%
)

# ── 4. 제약 조건 ──────────────────────────────────────────
# 부서별 가용 인력 상한
prob += x1 + x2 <= 8    # 기관부 최대 8명
prob += x3 + x4 <= 6    # 전자부 최대 6명
prob += x5 <= 4          # 무장부 최대 4명

# ── 5. 풀기 ───────────────────────────────────────────────
prob.solve(pulp.PULP_CBC_CMD(msg=0))

# ── 6. 결과 출력 ──────────────────────────────────────────
print("=== 수리창 5척 인력 배분 최적화 결과 ===")
print(f"최적화 상태: {pulp.LpStatus[prob.status]}\n")

함정목록 = [
    ("PKG-719 천안함", x1, 5, 5),
    ("FFG-815 충남함", x2, 4, 4),
    ("SS-083 나대용함", x3, 3, 6),
    ("PKG-711 독도함", x4, 2, 4),
    ("FF-951 울산함",  x5, 1, 3),
]

print(f"{'함정':<16} {'배치인력':>6} {'완료일':>8} {'가중진도율':>10}")
print("-" * 45)

total_weighted = 0
for 이름, 변수, 가중치, 진도율 in 함정목록:
    인력 = int(pulp.value(변수))
    완료일 = 100 / (인력 * 진도율)
    가중진도 = 가중치 * 진도율 * 인력
    total_weighted += 가중진도
    print(f"{이름:<16} {인력:>5}명 {완료일:>7.1f}일 {가중진도:>10.0f}")

print("-" * 45)
print(f"{'합계':<16} {'':>6} {'':>8} {total_weighted:>10.0f}")

# ── 7. 부서별 인력 사용 현황 ─────────────────────────────
print("\n=== 부서별 인력 사용 현황 ===")
기관부 = int(pulp.value(x1) + pulp.value(x2))
전자부 = int(pulp.value(x3) + pulp.value(x4))
무장부 = int(pulp.value(x5))
print(f"기관부: {기관부}명 / 8명 사용 (여유 {8-기관부}명)")
print(f"전자부: {전자부}명 / 6명 사용 (여유 {6-전자부}명)")
print(f"무장부: {무장부}명 / 4명 사용 (여유 {4-무장부}명)")

# ── 8. 균등 배분과 비교 ───────────────────────────────────
print("\n=== 균등 배분 대비 비교 ===")
# 균등: 각 부서 인력을 해당 함정에 균등 배분
균등배분 = {
    "PKG-719 천안함": 4, "FFG-815 충남함": 4,
    "SS-083 나대용함": 3, "PKG-711 독도함": 3,
    "FF-951 울산함": 4
}
균등가중 = (5*5*4 + 4*4*4 + 3*6*3 + 2*4*3 + 1*3*4)
print(f"균등 배분 가중 진도율: {균등가중}")
print(f"최적 배분 가중 진도율: {total_weighted:.0f}")
print(f"개선율: {(total_weighted - 균등가중) / 균등가중 * 100:.1f}%")